# Bankomat simulyatori
from IPython.display import clear_output
from termcolor import colored

balance = 500000  # boshlang'ich balans
password = "1234"
max_attempts = 3
attempts = 0

print(colored("üèß Bankomatga xush kelibsiz!", "cyan"))

while attempts < max_attempts:
    user_password = input("Parolni kiriting: ")
    if user_password == password:
        print(colored("‚úî Muvaffaqiyatli kirdingiz!\n", "green"))
        break
    else:
        attempts += 1
        print(colored(f"‚ùå Parol noto'g'ri! Qolgan urinishlar: {max_attempts - attempts}", "red"))
else:
    print(colored("üîí Siz 3 marta noto'g'ri kirdingiz. Tizim bloklandi!", "red"))
    raise SystemExit

# Interaktiv menyu
while True:
    print("\n--- M E N U ---")
    print("1. Balansni ko'rish")
    print("2. Pul yechish")
    print("3. Pul qo'yish")
    print("4. Chiqish")

    choice = input("Tanlang (1/2/3/4): ")

    if choice == "1":
        print(colored(f"üí∞ Sizning balansingiz: {balance} so'm", "cyan"))

    elif choice == "2":
        try:
            amount = int(input("Yechmoqchi bo'lgan summani kiriting: "))
            if amount <= 0:
                print(colored("‚ùå Iltimos, musbat summa kiriting!", "red"))
            elif amount <= balance:
                balance -= amount
                print(colored(f"‚úî {amount} so'm yechildi. Yangi balans: {balance} so'm", "green"))
            else:
                print(colored("‚ùå Balansda yetarli mablag' yo'q!", "red"))
        except ValueError:
            print(colored("‚ùå Iltimos, to'g'ri raqam kiriting!", "red"))

    elif choice == "3":
        try:
            amount = int(input("Kiritmoqchi bo'lgan summani kiriting: "))
            if amount <= 0:
                print(colored("‚ùå Iltimos, musbat summa kiriting!", "red"))
            else:
                balance += amount
                print(colored(f"‚úî {amount} so'm qo'shildi. Yangi balans: {balance} so'm", "green"))
        except ValueError:
            print(colored("‚ùå Iltimos, to'g'ri raqam kiriting!", "red"))

    elif choice == "4":
        print(colored("üëã Tizimdan chiqdingiz. Xayr!", "cyan"))
